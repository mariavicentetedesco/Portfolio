//Function to make pic appear and dissapear

let project1 = document.getElementById('company-home');
let pic1 = document.getElementById('company-pic');

project1.onclick = function showProject() {
    if (pic1.style.display === 'block') {
        pic1.style.display = 'none';
    } else {
    pic1.style.display = 'block';
    }
}



